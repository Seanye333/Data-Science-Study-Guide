def make_nb(sections):
    cells = []

    def md_cell(src):
        return {"cell_type": "markdown", "metadata": {}, "source": [src]}

    def code_cell(src):
        return {
            "cell_type": "code",
            "execution_count": None,
            "metadata": {},
            "outputs": [],
            "source": [src],
        }

    cells.append(md_cell("# Streamlit Study Guide\n\nBuild and deploy data apps in pure Python — no frontend skills needed.\n\n**Topics:** Setup, Text/Markdown, Input Widgets, Data Display, Charts, Layout, Sidebar & Session State, File Upload/Download, Forms & Caching, Multi-Page & Deployment"))

    for s in sections:
        cells.append(md_cell(f"## {s['title']}\n\n{s['desc']}"))
        cells.append(md_cell(f"### {s['code1_title']}"))
        cells.append(code_cell(s["code1"]))
        cells.append(md_cell(f"### {s['code2_title']}"))
        cells.append(code_cell(s["code2"]))
        cells.append(md_cell(f"### {s['code3_title']}"))
        cells.append(code_cell(s["code3"]))
        if s.get("code4_title"):
            cells.append(md_cell(f"### {s['code4_title']}"))
            cells.append(code_cell(s["code4"]))
        cells.append(md_cell(f"### Real-World Use Case\n\n**Scenario:** {s['rw_scenario']}"))
        cells.append(code_cell(s["rw_code"]))

        practice = s.get("practice")
        if practice:
            cells.append(md_cell(
                f"### \U0001f3cb\ufe0f Practice: {practice['title']}\n\n"
                f"> Save as `app.py` and run: `streamlit run app.py`\n\n"
                f"{practice['desc']}"
            ))
            cells.append(code_cell(practice["starter"]))

    return {
        "nbformat": 4,
        "nbformat_minor": 5,
        "metadata": {
            "kernelspec": {"display_name": "Python 3", "language": "python", "name": "python3"},
            "language_info": {"name": "python", "version": "3.10.0"},
        },
        "cells": cells,
    }


