    def code_cell(src):
        return {
            "cell_type": "code",
            "execution_count": None,
            "metadata": {},
            "outputs": [],
            "source": [src],
        }

    cells.append(md_cell("# Streamlit Study Guide\n\nBuild and deploy data apps in pure Python — no frontend skills needed.\n\n**Topics:** Setup, Text/Markdown, Input Widgets, Data Display, Charts, Layout, Sidebar & Session State, File Upload/Download, Forms & Caching, Multi-Page & Deployment"))

